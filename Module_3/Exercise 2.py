cabin=input("Please, enter the cabin class: ")
if cabin=="LUX":
    print("These are upper-deck cabins with balcony.")
elif cabin=="A":
    print("These are window equipped cabins above the car deck.")
elif cabin=="B":
    print("These are windowless cabins above the car deck.")
elif cabin=="C":
    print("These are windowless cabins below the car deck.")
else:
    print("Invalid cabin class")



